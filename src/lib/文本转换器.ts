// 转换函数
function 转换文本(text: string, 映射表: { [key: string]: string }): string {
  return text.split('').map(char => 映射表[char] || char).join('');
}

function 倒置文本(text: string): string {
  return text.split('').map(char => 倒置映射[char] || char).reverse().join('');
}

